package com.seleniumjava.java;

public class DowhileLoop
{

	public static void main(String[] args)
	{
	//	DowhileLoop:
		//it is used to execute the statements multiple times ,when we do not know the exact no of iteration
		
				//syntax:

				/*
				 * do { sts; }while(condition);
				 */
		int i=1;
		
		do {
			System.out.println("selenium");
			i++;
		}while(i>=20);//1>=20
	}

}
