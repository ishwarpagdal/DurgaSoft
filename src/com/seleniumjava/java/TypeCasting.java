package com.seleniumjava.java;

public class TypeCasting {

	public static void main(String[] args) 
	{
		
		//implicit type casting
		/*
		 * int a=10;
		 * 
		 * System.out.println(a);
		 * 
		 * double d=a; System.out.println(d);
		 */
		
		//explict 
		double d=12.0;
		
		System.out.println(d);
		
		int a=(int)d;
		
		System.out.println(a);

	}

}
